var btn1_click = document.querySelector(".btn1")

btn1_click.addEventListener('click', function() {
    var role1 = document.querySelector(".role1")
    var role_summ = document.querySelector(".role-summary")
    if (role1.style.display === "none") {
        role1.style.display = "block";
        role_summ.style.display = "none";
    } else {
        role1.style.display = "none";
        role_summ.style.display = "block";
    }
})

